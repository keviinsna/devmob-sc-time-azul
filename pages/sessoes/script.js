/**
 * Renderizando as abas
 */
const datas = Array.from(new Set(json.palestras.map((p) => p.data).sort()));
const tabs = document.getElementsByClassName("tabs")[0];
datas.forEach((data, idx) => {
  const button = document.createElement("button");
  button.type = "button";
  button.className = "tab";

  if (idx == 0) button.className += " active";

  button.onclick = (evt) => mostrarPalestras(evt, data);
  button.append(data);
  tabs.appendChild(button);
});

/**
 * Renderizando as palestras
 */
const cards = document.getElementsByClassName("tabsContent")[0];
json.palestras.forEach((p) => {
  const card = cardPalestra(p);
  if (p.data != "16 OUT") {
    card.style.display = "none";
  }
  cards.appendChild(card);
});

function mostrarPalestras(evt, aba) {
  // Marca aba atual como ativa e desativa as outras
  const abas = document.getElementsByClassName("tab");
  for (i = 0; i < abas.length; i++) {
    abas[i].className = abas[i].className.replace(" active", "");
  }
  evt.currentTarget.className += " active";

  // Esconde todos os cards
  const cards = document.getElementsByClassName("card");
  for (i = 0; i < cards.length; i++) {
    cards[i].style.display = "none";
  }

  // Mostra os cards da aba selecionada
  json.palestras.forEach((p, idx) => {
    if (p.data == aba) cards[idx].style.display = "block";
  });
}

function cardPalestra(palestra) {
  // card
  const article = document.createElement("article");
  article.className = "card";

  // card header
  const header = document.createElement("header");
  const title = document.createElement("h1");
  title.append(palestra.titulo);
  // hedaer

  const divIcons = document.createElement("div");
  divIcons.className = "icones";

  // span
  const spanHora = document.createElement("span");
  const spanLocal = document.createElement("span");
  const spanDuracao = document.createElement("span");

  // ícones
  const iconeHora = document.createElement("i");
  iconeHora.className = "fa-solid fa-clock";
  const iconeLocal = document.createElement("i");
  iconeLocal.className = "fa-solid fa-location-dot";
  const iconeDuracao = document.createElement("i");
  iconeDuracao.className = "fa-solid fa-hourglass";

  spanHora.append(iconeHora, palestra.hora);
  spanLocal.append(iconeLocal, palestra.local);
  spanDuracao.append(iconeDuracao, palestra.duracao);

  // main
  const main = document.createElement("main");
  const p = document.createElement("p");
  p.append(palestra.descricao);

  const containerPalestrantes = document.createElement("div");
  containerPalestrantes.className = "containerPalestrantes";
  palestra.palestrantes.forEach((p) => {
    const _palestrante = palestrante(p);
    containerPalestrantes.append(_palestrante);
  });

  // Append tags
  divIcons.append(spanHora, spanLocal, spanDuracao);
  header.append(divIcons, title);

  main.append(p);
  article.append(header, main, containerPalestrantes);

  return article;
}

function palestrante(_palestrante) {
  const divPalestrante = document.createElement("div");
  divPalestrante.className = "palestrante";

  const img = document.createElement("img");
  img.src = "../../images/palestrantes/user.png";

  const infos = document.createElement("div");
  infos.className = "infos";

  const nome = document.createElement("span");
  const cargo = document.createElement("span");
  const empresa = document.createElement("span");

  nome.append(_palestrante.nome);
  cargo.append(_palestrante.cargo);
  empresa.append(_palestrante.empresa);

  infos.append(nome, cargo, empresa);
  divPalestrante.append(img, infos);

  return divPalestrante;
}
